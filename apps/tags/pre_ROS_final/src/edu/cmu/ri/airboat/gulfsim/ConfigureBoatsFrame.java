    private void createSimBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createSimBActionPerformed
        double lat = Double.parseDouble(latSim.getText());
        double lon = Double.parseDouble(lonSim.getText());

        int port = (Integer) simPortNo.getValue();
        int count = (Integer) simNoS.getValue();

        for (int i = 0; i < count; i++) {
            
            System.out.println("Creating simulated");
            AirboatDummy dummy = new AirboatDummy(lat, lon);
            
            try {
                XmlRpcServer _server = new XmlRpcServer(port + i);
                _server.registerProxyingHandler(null, "^control\\.(.*)", dummy);
                _server.registerProxyingHandler(null, "^command\\.(.*)", dummy);
                _server.start();
            } catch (IOException ex) {
                Logger.getLogger(AirboatDummy.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            proxyManager.createSimulatedBoatProxy(port + i);
        }

        simPortNo.setValue(port + count);
    }//GEN-LAST:event_createSimBActionPerformed

