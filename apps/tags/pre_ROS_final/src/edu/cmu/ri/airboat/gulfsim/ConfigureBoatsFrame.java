    private void createVBSBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createVBSBActionPerformed

        double lat = Double.parseDouble(latVBS.getText());
        double lon = Double.parseDouble(lonVBS.getText());
        UTMCoord utm = UTMCoord.fromLatLon(Angle.fromDegrees(lat), Angle.fromDegrees(lon));

        String server = vbsServerF.getText();

        // What needs to happen here?
        double[] position = {12786.2, 15830.7, 0.00140381, 0.0, 0.0, 0.0};

        int port = (Integer) vbsPortNo.getValue();
        int count = (Integer) vbsNoS.getValue();
        for (int i = 0; i < count; i++) {
            AirboatVbs.startRpcInstance(server, port + i, position);
            
            proxyManager.createVBSBoatProxy(port + i);
        }

        vbsPortNo.setValue(port + count);
    }//GEN-LAST:event_createVBSBActionPerformed

