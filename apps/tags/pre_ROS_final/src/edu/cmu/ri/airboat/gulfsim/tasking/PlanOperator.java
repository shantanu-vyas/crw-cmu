package edu.cmu.ri.airboat.gulfsim.tasking;

public class PlanOperator extends PlanElement {
    private Role role = null;

    public PlanOperator(String name) {
        super(name);
    }
    
    public PlanOperator(String name, Role role) {
        super(name);
        this.role = role;
    }

    public Role getRole() {
        return role;
    }
    
}
